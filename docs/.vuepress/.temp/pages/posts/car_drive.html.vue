<p>四轮驱动，是指汽车前后轮都有动力，可按行驶路面状态不同而将发动机输出扭矩按不同比例分布在前后所有的轮子上，以提高汽车的行驶能力。一般用<code v-pre>4X4</code>或<code v-pre>4WD</code>来表示。</p>
<p><strong>四轮驱动（4-Wheel Drive，4WD）</strong> 是 <strong>全轮驱动（All Wheel Drive，AWD）</strong> 的子集，全轮驱动下面还有：6轮驱动、8轮驱动等。</p>
<ul>
