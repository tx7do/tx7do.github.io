<p>两轮驱动，即是由发动机通过变速箱等直接驱动前轮或者后轮，使汽车前进。</p>
<ul>
<li><strong>前轮驱动（Front-Wheel Drive）</strong> - 发动机驱动前面一对车轮</li>
